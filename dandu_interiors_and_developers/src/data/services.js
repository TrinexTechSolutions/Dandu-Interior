const recursiveMapImages = (item) => {
  const mapped = {
    ...item,
    image: getServiceImage(item.title || item.name, item.image)
  };

  if (item.subServices) {
    mapped.subServices = item.subServices.map(recursiveMapImages);
  }

  if (item.nestedServices) {
    mapped.nestedServices = item.nestedServices.map(recursiveMapImages);
  }

  return mapped;
};

